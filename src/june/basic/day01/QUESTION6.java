public class QUESTION6 {
	public static void main(String[] args) {
		double x;
		double y;
		int m;
		int n;

		x = 2.5;
		y = 1.5;
		m = 18;
		n = 4;

		System.out.print("가 = ");
		System.out.println(x + (n * y) - (x + n * y));
		System.out.print("나 = ");
		System.out.println((m / n) + (m % n));
		System.out.print("다 = ");
		System.out.println((5 * x) - (n / 5));
		System.out.print("라 = ");
		System.out.println(1 - (1 - (1 - (1 - (1 - n)))));
	}
}